class HarryAgeCalculator {
    public static void main(String[] args) {
        String nameA = "Harry";
        int birthYearA = 2000;
        int currentYearA = 2024;
        int ageA = currentYearA - birthYearA;

        System.out.println(nameA + "'s age in " + currentYearA + " is " + ageA);
    }
}
